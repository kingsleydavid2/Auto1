package JavaOOPscontinuation1;
import JavaOOPscontinuation1.*;

//Method Overriding -2

public class Program2 {
	
	//Overriding method
	public void eat() {
		System.out.println("Boy is eating");
	}

	
	public static void main(String[] args) {
		
		Program2 obj = new Program2();
		obj.eat();
		
		
	}
	
	
}
