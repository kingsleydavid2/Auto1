package JavaBasics;

//Operators = Comparison Operators, Bitwise Operators

public class Program7 {
	
	public static void main(String[] args) {
		
		int num1 = 100;
		int num2 = 20;
		
		if(num1==num2) {
			System.out.println("num1 and num2 are equal");
		} 
		else {
			System.out.println("num1 and num2 are not equal");
		}
		
		
		if(num1>num2) {
			System.out.println("num1 is greater then num2");
			
		} else {
			System.out.println("num1 is not greater then num2");
		}
		
		if(num1>=num2) {
			System.out.println("num1 is greater then or equal to num2");
		} else {
			System.out.println("num1 is not greater then or equal to num2");
		}
		
		
		
	}

}
