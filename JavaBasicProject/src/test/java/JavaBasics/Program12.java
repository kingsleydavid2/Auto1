package JavaBasics;

// Java Control statement - Switch Case

public class Program12 {
	
	
	public static void main(String[] args) {
		
       int i = 3;
       switch(i)
       {
    	   
       		case 1:
    		   System.out.println("Case1 ");
    		   break;
    		   
       		case 2:
       			System.out.println("Case2 ");
       			break;
       			
       		case 3:
       			System.out.println("Case3 ");
       			break;
       			
       		default:
       			System.out.println("Default ");
       		 
    	   
       }
		
	}
	
	
	

}
